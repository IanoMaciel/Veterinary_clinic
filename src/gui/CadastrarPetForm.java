    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        // EXCLUIR AQUI 
        
        if(jPet.getSelectedRow() != -1)
        {
            CadastrarPetDAO cadPet = new CadastrarPetDAO();
            Pet pet = new Pet();
            
            pet.setIdPet((Long)jPet.getValueAt(jPet.getSelectedRow(),0));
            
            cadPet.Deletar(pet);
            LimparCampos();
            
            try {
                exibirTablePet();
            } catch (SQLException ex) {
                Logger.getLogger(CadastrarPetForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

