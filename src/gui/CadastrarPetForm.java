    public void exibirJTable() throws SQLException
    {
        DefaultTableModel modelo = (DefaultTableModel) jTableCliente.getModel();
        modelo.setNumRows(0);
        
        CadastrarDonoDAO c = new CadastrarDonoDAO();
        
        for(Dono d : c.Exibir())
        {
            modelo.addRow(new Object[]
            {
                d.getIdPessoaDono(),
                d.getNome(),
                d.getCpf(),
               
            });
        }
    }
    
    public void exibirTablePet() throws SQLException
    {
        DefaultTableModel modelo = (DefaultTableModel) jPet.getModel();
        modelo.setNumRows(0);
        
        CadastrarPetDAO c = new CadastrarPetDAO();
        
        for(Pet p : c.ExibirPet())
        {
            modelo.addRow(new Object[]
            {
                p.getIdPet(),
                p.getNome(),
                p.getPorte(),
                p.getPeso(),
                p.getDescricao(),
                p.getIdPessoaDono()
            });
        }
        
    }
    
